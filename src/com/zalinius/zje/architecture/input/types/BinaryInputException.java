package com.zalinius.zje.architecture.input.types;

public class BinaryInputException extends IllegalStateException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 6610358790909296777L;

	public BinaryInputException() {
		super();
	}

	public BinaryInputException(String message, Throwable cause) {
		super(message, cause);
	}

	public BinaryInputException(String s) {
		super(s);
	}

	public BinaryInputException(Throwable cause) {
		super(cause);
	}
	
	

}
